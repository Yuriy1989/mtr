export const statusColor = (code) => {
  const c = normalizeStatus(code);
  if (c === 0) return "red";
  if (c === 10) return "default";
  if (c === 20) return "magenta";
  if (c === 30) return "blue";
  if (c === 40) return "purple";
  if (c === 50) return "geekblue";
  if (c === 60) return "cyan";
  if (c === 100) return "green";
  if (c === 110) return "gold";
  if (c === 115) return "orange";
  if (c === 120) return "volcano";
  if (c === 130) return "volcano";
  return "default";
};

